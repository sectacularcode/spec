// src/utils/htmlEscape.js
// Shared HTML escape utility used by both Template Studio and Brief to Blueprint.

export function he(s) {
  if (s == null) return "";
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
